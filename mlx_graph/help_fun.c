#include "ft_fractol_mlx.h"

int			rgb_to_int(t_color cl)
{
	int	rgb;

	rgb = ((int)(cl.r) << 16) + ((int)(cl.g) << 8)
	+ (int)(cl.b);
	return (rgb);
}
